package leksion1;

public class Variablat {
    public static void main(String []args){
        int x;
        x=10;
        System.out.println(x);
        char c='A';
        System.out.println(c);
        String S="SD Academy";
        System.out.println(S);
        float nrf=10.5f;
        double dValue=23.42d;
        System.out.println(dValue);
        System.out.println(nrf);
        boolean bl=true;
        System.out.println(bl);
        final int vlereFinale=10;
        System.out.println(vlereFinale);
    }
}
